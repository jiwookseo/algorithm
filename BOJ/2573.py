from pprint import pprint as pp
dr = [1, 0, -1, 0]
dc = [0, -1, 0, 1]
visited = []


class Queue:
    def __init__(self):
        self.size = 300
        self.init = None
        self.front = 0
        self.rear = 0
        self.storage = [self.init] * self.size

    def enQueue(self, item):
        self.rear = (self.rear + 1) % self.size
        self.storage[self.rear] = item
        return

    def deQueue(self):
        self.front = (self.front + 1) % self.size
        return self.storage[self.front]

    def isEmpty(self):
        return self.front == self.rear


def warming():
    global im, visited, dr, dc
    for r in range(n):
        for c in range(m):
            if visited[r][c] != -1:
                im[r][c] = im[r][c] - visited[r][c] if im[r][c] > visited[r][c] else 0


def check2(r, c):
    global im, visited, dr, dc, q
    for i in range(4):
        tr, tc = r + dr[i], c + dc[i]
        if 0 <= tr < n and 0 <= tc < m:
            if im[tr][tc] > 0:
                visited[r][c] -= 1
                if visited[tr][tc] == -1:
                    visited[tr][tc] = 4
                    q.enQueue((tr, tc))


def check():
    global im, visited, q
    res = 0
    q = Queue()
    visited = [[-1]*m for _ in range(n)]
    while True:
        cc = False
        for r in range(n):
            for c in range(m):
                if im[r][c] > 0 and visited[r][c] == -1:
                    cc = True
                    break
            if cc:
                break
        if cc:
            res += 1
            q.enQueue((r, c))
            visited[r][c] = 4
            while not q.isEmpty():
                check2(*(q.deQueue()))
        else:
            return res


inp = input().split()
n, m = int(inp[0]), int(inp[1])
im = [list(map(int, input().split())) for _ in range(n)]
t = 0
while True:
    temp = check()
    if temp == 0:
        result = 0
        break
    elif temp >= 2:
        result = t
        break
    else:
        t += 1
        warming()
print(result)

