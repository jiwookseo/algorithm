from pprint import pprint as pp


def dijkstra(s):
    global g, v, n
    u = {s}
    while u != v:
        temp = v - u
        mini = 1000
        w = 0
        for e in temp:
            if mini > g[s][e]:
                mini = g[s][e]
                w = e
        u.add(w)
        for i in range(n):
            if 0 < g[w][i] < 100:
                g[s][i] = min(g[s][i], g[s][w] + g[w][i])


n = int(input())
inp = [list(map(int, input().split())) for _ in range(n)]
v = set(range(n))
for i in range(n):
    g = [r[:] for r in inp[:]]
    dijkstra(i)
    print("dijkstra({})".format(i))
    pp(g)

